#!/usr/bin/env python3
"""This module defines a concat function."""


def concat(str1: str, str2: str) -> str:
    """
    Concatene two strings and return his result.

    Args:
        str1 (str): The first string to concatene.
        str2 (str): The second string to concatene.

    Returns:
        str: The concatened string.
    """
    return str1 + str2
